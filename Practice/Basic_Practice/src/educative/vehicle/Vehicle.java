package vehicle;

public class Vehicle {
    private String speed;
    private String model;

    public String getModel() {
        return model;
    }

    public String getSpeed() {
        return speed;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }
}
